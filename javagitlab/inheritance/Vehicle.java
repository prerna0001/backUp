package com;

public class Vehicle
{
    private int gear;
    private int speed;

    public int getGear()
    {
        return gear;
    }

    public void setGear(int gear)
    {
        this.gear = gear;
    }

    public int getSpeed()
    {
        return speed;
    }

    public void setSpeed(int speed)
    {
        this.speed = speed;
    }
}
